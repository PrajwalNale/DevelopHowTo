                        <h2>How Blockchain works</h2>
                        <p class="color-2">Bitcoin blockchain explorer and analytics search engine Blockchair has announced that it is now backed by Bitmain, a leading mining hardware manufacturer. The company also made its intentions to extend its support to the Ethereum blockchain known in the same announcement.</p>

                        <b>Wallet</b>

                        <p>A wallet is a string of numbers and letters, such as 18c177926650e5550973303c300e136f22673b74.
                                       This is an address that will appear in various blocks within the Blockchain as transactions take place.
                                        No visible records of who did what transaction with who, only the number of a wallet.
                                       The address of each particular wallet is also a public key.</p>

                        <b>Digital Signature</b>

                        <p>To carry out a transaction you need two things: a wallet, which is basically an address, and a private key.
                                       The private key is a string of random numbers, but unlike the address the private key must be kept secret.</p>

                        <b>Protocols</b>

                        <p>The Blockchain consists of individual behaviour specifications, a large set of rules
                                       that are programmed into it. Those specifications are called protocols. The implementation
                                      of specific protocols essentially made Blockchain what it is — a distributed,
                                       peer-to-peer and secured information database.</p>

                        <b>Mining</b>

                        <p>Miners on a Blockchain are nodes that produce blocks by solving proof of work
                                      problems. If a miner produces a block that is approved by an electronic consensus of
                                      nodes then the miner is rewarded with coins.
                                      As of October 2017, Bitcoin miners get 12.5 Bitcoins per block.</p>
